CREATE OR REPLACE TYPE TYP_READ_REQUEST_NOTIFICATION_OBJ AS OBJECT
(
  APP_USER_ID       NUMBER(20),
  MRC_IDENTIFY      VARCHAR2(30),
  REQUEST_MASTER_ID NUMBER(19),
  STATUS            NUMBER(10)
